package com.company.services;

public class Calculator {
    public static int addResult(int number1, int number2){
        return number1 + number2;
    }

    public static int subResult(int number1, int number2){
        return number1 - number2;
    }

    public static int multResult(int number1, int number2){
        return number1 * number2;
    }

    public static float divResult(int number1, int number2){
        return ((float)number1 / number2);
    }
}
